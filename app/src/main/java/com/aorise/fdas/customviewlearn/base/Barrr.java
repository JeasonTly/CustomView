public class Barrr {
    private int score;
    private String itemName;

    public Barrr(int score, String itemName) {
        this.score = score;
        this.itemName = itemName;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }
}
